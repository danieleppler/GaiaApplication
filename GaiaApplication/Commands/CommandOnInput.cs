namespace GaiaApplication.Commands
{
    public class CommandOnInput
    {

        public int addNumbers(int a, int b)
        {
            return a + b;
        }

        public int SubstractNumbers(int a,int b)
        {
            return a - b;
        }

        public string concatStrings(string a, string b)
        {
            return a + b;
        }
    }
}
